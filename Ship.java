public class Ship {

    public static int health;
    public static int speed;
    public static int dodge;
    public static int armor;
    public static int accuracy;
    public static int damage;


    public Ship(int health, int speed, int dodge, int armor, int accuracy, int damage)
    {
        this.health = health;
        this.speed = speed;
        this.dodge = dodge;
        this.armor = armor;
        this.accuracy = accuracy;
        this.damage = damage;
    }
}
